package msdchallenge.model;

public class Track {

	public final String id1;
	public final String trackId;
	public final String artist;
	public final String song;

	public Track(String id1, String trackId, String artist, String song) {
		this.id1 = id1;
		this.trackId = trackId;
		this.artist = artist;
		this.song = song;
	}

}
